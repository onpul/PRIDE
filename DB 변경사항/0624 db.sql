         , CONTENT
        FROM INQUIRY I, INQUIRY_PROCESS P, PROFILE PF
        WHERE I.INQUIRY_ID = P.INQUIRY_ID(+)
          AND I.USER_ID = PF.USER_ID(+)
    ) RESULT
    WHERE RESULT.RANK = 1
    ORDER BY NUM DESC
